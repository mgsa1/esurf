/**
 * Renders the full sprite sheet to an OffscreenCanvas (or regular Canvas).
 * Returns the canvas plus metadata for UV mapping.
 *
 * The sheet is 288×48 (6 frames × 48px wide, 48px tall).
 */
export function createSpriteSheet(): {
  canvas: HTMLCanvasElement | OffscreenCanvas;
  frameCount: number;
  frameWidth: number;
  frameHeight: number;
} {
  const useOffscreen = typeof OffscreenCanvas !== 'undefined';
  const canvas = useOffscreen
    ? new OffscreenCanvas(SHEET_WIDTH, SHEET_HEIGHT)
    : document.createElement('canvas');

  if (!useOffscreen) {
    (canvas as HTMLCanvasElement).width = SHEET_WIDTH;
    (canvas as HTMLCanvasElement).height = SHEET_HEIGHT;
  }

  const ctx = canvas.getContext('2d') as CanvasRenderingContext2D | OffscreenCanvasRenderingContext2D;
  if (!ctx) throw new Error('Failed to get 2d context for sprite sheet');

  ctx.clearRect(0, 0, SHEET_WIDTH, SHEET_HEIGHT);

  for (let f = 0; f < FRAMES.length; f++) {
    const frame = FRAMES[f];
    const ox = f * FRAME_WIDTH;

    for (let y = 0; y < frame.length && y < FRAME_HEIGHT; y++) {
      const line = frame[y];
      for (let x = 0; x < line.length && x < FRAME_WIDTH; x++) {
        const ch = line[x];
        if (ch === '.') continue;
        const color = PAL[ch];
        if (color) {
          ctx.fillStyle = color;
          ctx.fillRect(ox + x, y, 1, 1);
        }
      }
    }
  }

  return {
    canvas,
    frameCount: FRAME_COUNT,
    frameWidth: FRAME_WIDTH,
    frameHeight: FRAME_HEIGHT,
  };
}
